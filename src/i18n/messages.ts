export type MessageKey = keyof typeof S.en;

export function strings(locale: AppLocale): typeof S.en {
  return (S[locale] ?? S.en) as typeof S.en;
}

export function tx(locale: AppLocale, key: MessageKey, vars?: Record<string, string | number>): string {
  const raw = strings(locale)[key];
  return vars ? fill(raw, vars) : raw;
}

/** Heuristic chat reply when DeepSeek is off (Jarviddin intro). */
